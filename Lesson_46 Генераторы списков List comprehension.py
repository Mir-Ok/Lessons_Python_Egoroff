# _5 вложенный цикл, фиксируется первая переменная, пока не прокрутится вся вторая, потом первая шагает вперед ...
#  в итоге все возможные сочетания

e = [(i, j) for i in 'abc' for j in [1, 2, 3]]
print(e)  # --> [('a', 1), ('a', 2), ('a', 3), ('b', 1), ('b', 2), ('b', 3), ('c', 1), ('c', 2), ('c', 3)]

f = [i * j for i in [1, 2, 3, 4, 5] for j in [1, 2, 3] if i * j >= 10]
print(f)  # --> [12, 10, 15]