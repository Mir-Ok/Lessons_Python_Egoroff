# обычная запись цикла for

a = []
for i in range(7):
    a.append(i)
print(a)  # --> [0, 1, 2, 3, 4, 5, 6]


# _1 шаблон генератора списков [выражение for val in коллекция]

a = [0 for i in range(7)]
print(a)  # --> [0, 0, 0, 0, 0, 0, 0]  # можно выводить константу нужное число раз

a = [i for i in range(7)]
print(a)  # --> [0, 1, 2, 3, 4, 5, 6]  # можно заменить константу переменной

a = [i**2 for i in range(7, 15)]  # можно изменять выводимую переменную
print(a)  # --> [49, 64, 81, 100, 121, 144, 169, 196]

a = [i for i in 'hello']  # можно обходить любые итерабельные объекты
print(a)  # --> ['h', 'e', 'l', 'l', 'o']

a = [ord(i) for i in 'hello']  # допустимо использовать функции внутри генератора
print(a)  # --> ['h', 'e', 'l', 'l', 'o']

import random
b = [random.randint(-10, 10) for i in range(10)]  # используем генераторы в качестве переменной
print(b)  # --> [5, -5, -3, -7, -2, 5, -7, 8, -7, 7]

c = [abs(elem) for elem in b]  # создаем переменные на основе элементов другого списка
print(c)  # --> [5, 5, 3, 7, 2, 5, 7, 8, 7, 7]

''' при помощи генераторов можно менять исходные последовательности '''

b = [elem+1 for elem in b]  # создаем переменные на основе элементов другого списка
print(b)  # --> [6, -4, -2, -6, -1, 6, -6, 9, -6, 8]


