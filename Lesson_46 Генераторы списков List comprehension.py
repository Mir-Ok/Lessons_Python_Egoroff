# # _2 шаблон генератора списков с условием [выражение for val in коллекция if условие]

import random
b = [random.randint(-10, 10) for i in range(10)]  # используем генераторы в качестве переменной
print(b)  # --> [-6, 0, -4, 9, 0, -2, 7, -5, -5, 10]

b = [i for i in b if i % 2 == 0]  # задаем условие отбора
print(b)  # --> [-6, 0, -4, 0, -2, 10]

b = [i for i in b if i % 2 == 0 and i % 3 == 0]  # задаем сложное условие
print(b)  # --> [-6, 0, 0]


